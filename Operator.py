a = 10
b= 20
# Arithmetic Operators
print("a + b =", a + b)  # Addition
print("a - b =", a - b)  # Subtraction
print("a * b =", a * b)  # Multiplication
print("a / b =", a / b)  # Division
print("a % b =", a % b)  # Modulus
print("a ** b =", a ** b)  # Exponentiation
print("a // b =", a // b)  # Floor Division
# Comparison Operators
print("a == b:", a == b)  # Equal to
print("a != b:", a != b)  # Not equal to
print("a > b:", a > b)    # Greater than
print("a < b:", a < b)    # Less than
print("a >= b:", a >= b)  # Greater than or equal to
print("a <= b:", a <= b)  # Less than or equal to
# Logical Operators
print("a > 5 and b < 30:", a > 5 and b < 30)  # Logical AND
print("a < 5 or b < 30:", a < 5 or b < 30)    # Logical OR
print("not (a > 5):", not (a > 5))  # Logical NOT
# Assignment Operators
a += 5  # Add and assign
print("After a += 5, a =", a)  # a = a + 5
b -= 5  # Subtract and assign
print("After b -= 5, b =", b)  # b = b - 5
a *= 2  # Multiply and assign
print("After a *= 2, a =", a)  # a = a * 2
b /= 2  # Divide and assign
print("After b /= 2, b =", b)  # b = b / 2
a %= 3  # Modulus and assign
print("After a %= 3, a =", a)  # a = a % 3
b **= 2  # Exponentiation and assign
print("After b **= 2, b =", b)  # b = b ** 2
a //= 2  # Floor division and assign
print("After a //= 2, a =", a)  # a = a // 2


b &= 3  # Bitwise AND and assign
print("After b &= 3, b =", b)  # b = b & 3
b |= 2  # Bitwise OR and assign
print("After b |= 2, b =", b)  # b = b | 2
b ^= 1  # Bitwise XOR and assign
print("After b ^= 1, b =", b)  # b = b ^ 1
